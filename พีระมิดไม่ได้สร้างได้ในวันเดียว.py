#พีระมิดเป็นหนึ่งในสิ่งมหัศจรรย์ของโลก ที่มีผู้คนพูดถึงมากมายเกี่ยวกับการก่อสร้างมัน บางคนก็ว่าอาจเกิดจากเทพเจ้า หรือ มนุษย์ต่างดาว 
# แม้ว่าหลักฐานที่เราเห็นแน่ชัดแล้วว่าจะมาจากคนธรรมดาที่มีความเชื่อที่จะส่งกษัตริย์แห่งอียิปต์ที่เขารักและเชิดชูกลับสวรรค์ก็ตาม

n = int(input())
for i in range(n):
    for j in range(n-i-1):
        print(" ",end="")
    for j in range (2*i+1):
        print("*",end="")
    print()